#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int	*ptr;

	ptr = (int *) s;
	if (!s)
		return (0);
	while (n)
	{
		ptr[n - 1] = c;
		n--;
	}
}
